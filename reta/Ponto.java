public class Ponto {
	private float x;
	private float y;

	public Ponto(float x, float y){
		this.x = x;
		this.y = y;
	}


	public void setX(float value) {this.x = value;}

	public float getX() {return this.x;}


	public void setY(float value) {this.y = value;}

	public float getY() {return this.y;}

}
